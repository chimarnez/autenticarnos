import { useState } from "react";
import Box from "@mui/material/Box";
import Stepper from "@mui/material/Stepper";
import Step from "@mui/material/Step";
import StepLabel from "@mui/material/StepLabel";
import StepContent from "@mui/material/StepContent";
import Button from "@mui/material/Button";
import Paper from "@mui/material/Paper";
import Typography from "@mui/material/Typography";
import { StepButton } from "@mui/material";

const steps = [
  {
    label: "Palma izquierda",
    description: `Coloca la palma de la mano izquierda en la cámara.`,
    name: "palmLeft",
  },
  {
    label: "Dorsal izquierda",
    description: `Gira la mano izquierda y toma una foto del dorso de la mano.`,
    name: "backLeft",
  },
  {
    label: "Palma derecha",
    description: `Ahora coloca la palma de la mano derecha`,
    name: "palmRight",
  },
  {
    label: "Dorsal derecha",
    description: `Por último, gira la mano derecha y toma una foto del dorso de la mano.`,
    name: "backRight",
  },
];

export function VerticalPhotoStepper() {
  const [activeStep, setActiveStep] = useState(0);
  //   const [imageStep, setImageStep] = useState<(HTMLImageElement | null)[]>([])
  const [imageStep, setImageStep] = useState<boolean[]>([
    false,
    false,
    false,
    false,
  ]);

  const handleNext = () => {
    const updatedImageStep = [...imageStep];
    updatedImageStep[activeStep] = true;
    setImageStep(updatedImageStep);
    setActiveStep((prevActiveStep) => prevActiveStep + 1);
  };

  const handleBack = () => {
    setActiveStep((prevActiveStep) => prevActiveStep - 1);
  };

  const handleReset = () => {
    setActiveStep(0);
  };

  return (
    <Box sx={{ maxWidth: 400 }}>
      <Stepper nonLinear activeStep={activeStep} orientation="vertical">
        {steps.map((step, index) => (
          <Step key={step.label} completed={imageStep[index]}>
            <StepButton onClick={() => setActiveStep(index)}>
              <StepLabel
                optional={
                  index === 2 ? (
                    <Typography variant="caption">Last step</Typography>
                  ) : null
                }
              >
                {step.label}
              </StepLabel>
            </StepButton>
            <StepContent>
              <Typography>{step.description}</Typography>
              <Box sx={{ mb: 2 }}>
                <div>
                  <Button
                    variant="contained"
                    onClick={handleNext}
                    sx={{ mt: 1, mr: 1 }}
                  >
                    {index === steps.length - 1 ? "Finalizar" : "Continuar"}
                  </Button>
                  <Button
                    disabled={index === 0}
                    onClick={handleBack}
                    sx={{ mt: 1, mr: 1 }}
                  >
                    Atrás
                  </Button>
                </div>
              </Box>
            </StepContent>
          </Step>
        ))}
      </Stepper>
      {activeStep === steps.length && (
        <Paper square elevation={0} sx={{ p: 3 }}>
          <Typography>All steps completed - you&apos;re finished</Typography>
          <Button onClick={handleReset} sx={{ mt: 1, mr: 1 }}>
            Reset
          </Button>
        </Paper>
      )}
    </Box>
  );
}

